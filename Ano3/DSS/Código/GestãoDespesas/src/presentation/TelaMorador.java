    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        int montante = Integer.parseInt(jTextField13.getText());
        String data = jTextField14.getText();
        String estado = jTextField15.getText();
        String descricao = jTextField16.getText();

        DespesaExtraordinaria a = new DespesaExtraordinaria (montante, data, estado, descricao);
        facade.removerDespesaExt(a);
        updateListDespesaExt();
    }//GEN-LAST:event_jButton9ActionPerformed

    private void jList5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList5MouseClicked
        int i = jList5.getSelectedIndex();
        DespesaExtraordinaria a = (DespesaExtraordinaria) facade.listDespesaExt().get(i);
        jTextField13.setText(String.valueOf(a.getMontante()));
        jTextField14.setText(a.getData());
        jTextField15.setText(a.getEstado());
        jTextField16.setText(a.getDescricao());
    }//GEN-LAST:event_jList5MouseClicked

