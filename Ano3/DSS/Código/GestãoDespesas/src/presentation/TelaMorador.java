    private void updateList () {
        DefaultListModel<Utilizador> l = new DefaultListModel<Utilizador>();
        try {
            for (Utilizador a: facade.listUtilizador())
                l.addElement(a);
        }
        catch (Exception e) {
        }
        
        jList1.setModel((ListModel) l);
        
    }
    
